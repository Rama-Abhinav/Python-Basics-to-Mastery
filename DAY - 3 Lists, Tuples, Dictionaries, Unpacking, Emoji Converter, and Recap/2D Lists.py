# Useful in machine learning and deep learning automation

matrix=[
    [1,2,3],
    [4,5,6],
    [7,8,9]
]

print(matrix)

# Indexing 2-D List
print(matrix[0][1]) ## This prints the 2nd element in the 1st row

print(matrix[-1][-2]) ## This prints the 2nd element from the last row (Negative Indexing)